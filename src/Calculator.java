public class Calculator {
    private int x, y;

    public Calculator(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int add() {
        int z = this.x + this.y;
        return z;
    }

    public int getpass() {
        int z = this.x + this.y;
        return z;
    }

    public int sub() {
        int z = this.x - this.y;
        return z;
    }
    public int getsub() {
        int z = this.x - this.y;
        return z;
    }

    public int mul() {
        int z = this.x * this.y;
        return z;
    }
    public int getmul() {
        int z = this.x * this.y;
        return z;
    }

    public int div() {
        int z = this.x / this.y;
        return z;
    }
    public int getdiv() {
        int z = this.x / this.y;
        return z;
    }


}
